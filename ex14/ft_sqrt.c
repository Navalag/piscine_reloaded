int		ft_sqrt(int nb)
{
	int i;
	int n;

	i = 0;
	n = 1;
	if (nb == 1)
		return (1);
	else if (nb > 1)
	{
		while (nb > n)
		{
			n = i * i;
			i++;
		}
	}
	i--;
	if (nb == n)
		return (i);
	return (0);
}
